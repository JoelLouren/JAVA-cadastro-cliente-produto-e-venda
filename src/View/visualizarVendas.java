    private void Tab_vendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tab_vendasMouseClicked
        // TODO add your handling code here:
        int codCliente = 0;
        cadastrarVenda cadVenda = new cadastrarVenda();
        int linha = Tab_vendas.getSelectedRow();
        Venda venda = new Venda();
        
        venda.setCodVenda(Integer.parseInt(Tab_vendas.getValueAt(linha, 0).toString()));
        venda.setNome(Tab_vendas.getValueAt(linha, 1).toString());
        try {
            codCliente = vendModel.retornaCliente(venda.getCodVenda());
        } catch (SQLException ex) {
            Logger.getLogger(visualizarVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
        venda.setCodCliente(codCliente);
        venda.setTot_venda(Float.parseFloat(Tab_vendas.getValueAt(linha, 3).toString()));
        
        this.setVisible(false);
        cadVenda.arrumaTelaTeste(venda);
        cadVenda.setVisible(true);

    }//GEN-LAST:event_Tab_vendasMouseClicked

