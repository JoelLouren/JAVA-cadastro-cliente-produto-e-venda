    public void arrumarTabela() {
      
            String sql = "select * from listaven;";
        ArrayList resultado =  vendModel.pesquisar2(sql);
            if (resultado != null) {
            DefaultTableModel modelo = (DefaultTableModel) Tab_vendas.getModel();
            //System.out.println(modelo.getRowCount());
            //System.out.println(resultado.size());
            while (modelo.getRowCount() != 0) {
                modelo.removeRow(0);
                            System.out.println(modelo.getRowCount());

            } 
            for (int i = 0; i < resultado.size(); i++) {
                Venda venda = new Venda();
                venda = (Venda) resultado.get(i);
                String[] linha = new String[4];
                linha[0] = String.valueOf(venda.getCodVenda());
                linha[1] = String.valueOf(venda.getNome());
                linha[2] = String.valueOf(venda.getEndereco());
                linha[3] = String.valueOf(venda.getTot_venda());
                modelo.addRow(linha);
                Tab_vendas.setModel(modelo);
            }
        } else {
            System.out.println("\nArrayList retornou nulo");
        }     
    }
    
    
    
    

