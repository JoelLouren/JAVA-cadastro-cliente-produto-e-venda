    public void arrumaTabela(String prod){
        ProdutoControl acessar = new ProdutoControl();
        lista = acessar.validarNomePesquisa(prod);
        if (lista != null) {
            DefaultTableModel modelo = (DefaultTableModel) tab_produto.getModel();
            while (modelo.getRowCount() != 0) {
                modelo.removeRow(0);
            }
            for (int i = 0; i < lista.size(); i++) {
                Produto p = new Produto();
                p = (Produto) lista.get(i);
                String[] linha = new String[3];
                linha[0] = String.valueOf(p.getCod());
                linha[1] = p.getNome();
                linha[2] = "R$ " + String.valueOf(p.getPreco());
                modelo.addRow(linha);
                tab_produto.setModel(modelo);
            }
        } else {
            System.out.println("\nArrayList retornou nulo");
        }        
        
    }
    
    
    
    
