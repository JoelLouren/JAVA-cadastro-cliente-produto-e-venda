    //ARRUMAR TELA PARA INSERT DE PRODUTOS
    public void arrumaInsert(){
        
        ct_nomeProd.setText("");
        ct_precoProd.setText("");
        bt_alterar.setEnabled(false);
        bt_excluir.setEnabled(false);
        bt_salvar.setEnabled(true);
        
    }
    
    
    
    //ARRUMAR TELA PARA UPDATE DE PRODUTOS
    public void arrumaUpdate(Produto p){
        
        ct_nomeProd.setText(p.getNome());
        ct_precoProd.setText(String.valueOf(p.getPreco()));
        ct_nomeProd.setEditable(false);
        ct_precoProd.setEditable(false);
        bt_salvar.setEnabled(false);
        bt_alterar.setEnabled(true);
        bt_excluir.setEnabled(true);
        idProd = p.getCod();
    }
    
