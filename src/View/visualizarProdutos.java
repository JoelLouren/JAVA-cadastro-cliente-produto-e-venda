                public void arrumaTabela() {
                    
                    
        ArrayList resultado = produtoModel.pesquisar(ct_nomeProd.getText());
        if (resultado != null) {
            DefaultTableModel modelo = (DefaultTableModel) tabela_produto.getModel();
            while (modelo.getRowCount() != 0) {
                modelo.removeRow(0);
            }
            for (int i = 0; i < resultado.size(); i++) {
                Produto prod = new Produto();
                prod = (Produto) resultado.get(i);
                String[] linha = new String[3];
                linha[0] = String.valueOf(prod.getCod());
                linha[1] = prod.getNome();
                linha[2] = String.valueOf(prod.getPreco());
                modelo.addRow(linha);
                tabela_produto.setModel(modelo);
            }
        } else {
            System.out.println("\nArrayList retornou nulo");
        }
    }
    
    
    
  
    
    
    
    
    
    
    
