    private void tabela_produtoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela_produtoMouseClicked
        // TODO add your handling code here:
        int clicado = tabela_produto.getSelectedRow();
        Produto prod = new Produto();
        prod.setCod(Integer.parseInt(tabela_produto.getValueAt(clicado,0).toString()));
        prod.setNome(tabela_produto.getValueAt(clicado,1).toString());
        prod.setPreco(Float.parseFloat(tabela_produto.getValueAt(clicado,2).toString()));
        this.setVisible(false);
        cadastroProduto cadPro = new cadastroProduto();
        cadPro.arrumaUpdate(prod);
        cadPro.setVisible(true);
        ct_nomeProd.setText("");
    }//GEN-LAST:event_tabela_produtoMouseClicked

